                self.errorMessage = "ÂèëÈÄÅÊ∂àÊÅØÊó∂Âá∫ÈîôÔºö\(error.localizedDescription)"
            }
        }
        // Ê∏ÖÁ©∫ËæìÂÖ•Ê°ÜÂíåÂõæÂÉè
        inputText = ""
        image = nil
    }
